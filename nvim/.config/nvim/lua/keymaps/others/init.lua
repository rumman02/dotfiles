local keybind_functions = require("keymaps.functions")
local map = vim.keymap.set
local opts = { noremap = true, silent = true }

--==================================================--
--                       copy                       --
--==================================================--
map({"v", "x"}, "<c-y>", '"+ygv <esc>h')
map("n", "<c-y>", '"+y')
map({"v", "x"}, "y", "ygv <esc>h" )
--==================================================--
--             incremental/decremenal               --
--==================================================--
map("n", "+", "<c-a>", opts)
map("n", "-", "<c-x>", opts)
--==================================================--
--                   indentation                    --
--==================================================--
map({ "v", "x" }, "<", "<gv", opts)
map({ "v", "x" }, ">", ">gv", opts)
--==================================================--
--                 cursor movement                  --
--==================================================--
map( "i", "<c-h>", "<left>", opts )
map( "i", "<c-l>", "<right>", opts )
-- map( "i", "<c-k>", "<up>", opts )
map( "i", "<c-j>", "<down>", opts )
map( "i", "<c-s-k>", "<esc>O", opts )
map( "i", "<c-s-j>", "<esc>o", opts )
--==================================================--
--                       fix                        --
--==================================================--
map("n", "q", function () keybind_functions.handle_q_colon_key() end, opts)
map({ "v", "x" }, "p", '"_dP', opts)
map( "n", "x", '"_x', opts )
map( "n", "j", "gj", opts )
map( "n", "k", "gk", opts )
--==================================================--
--                     moveline                     --
--==================================================--
map({ "v", "x" }, "<a-k>", ":m '<-2<cr>gv=gv", opts )
map({ "v", "x" }, "<a-j>", ":m '>+1<cr>gv=gv", opts )
map({ "v", "x" }, "<a-h>", "<gv", opts )
map({ "v", "x" }, "<a-l>", ">gv", opts )
--==================================================--
--                       save                       --
--==================================================--
map({ "n", "i", "v", "x" }, "<c-q>", "<cmd>w<cr>", opts )
